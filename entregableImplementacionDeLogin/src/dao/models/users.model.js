import mongoose from "mongoose";

const collectionName = "Usuarios";
const userSchema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true,
    },
    last_name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    age: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: "usuario",
    },
});
const userModel = mongoose.model(collectionName, userSchema);
export default userModel;